    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
           if (jtfIDSP.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Mã sản phẩm không được rỗng");
                jtfIDSP.requestFocus();
                return;
            } else if (jtfTenSP.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Tên sản phẩm không được rỗng");
                jtfIDSP.requestFocus();
                return;
            } else if (jtfSoLuong.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Số lượng không được rỗng");
                jtfIDSP.requestFocus();
                return;
            } else if (jtfGia.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Giá không được rỗng");
                jtfIDSP.requestFocus();
                return;
            }
            try {
            Connection connection = DBConnection.getConnection();
            String sql = "INSERT INTO dbo.[sanpham](IDSP,tensp,gia,soLuong)" +  "VALUES(?,?,?,?)";
            
            PreparedStatement ps = connection.prepareStatement(sql);
            ps.setString(1,jtfIDSP.getText());
            ps.setNString(2,jtfTenSP.getText());
            ps.setString(3, jtfGia.getText());
            ps.setString(4, jtfSoLuong.getText());
            
            ps.executeUpdate();
            showDuLieu();
            DBConnection.closeConnection(connection);
            JOptionPane.showMessageDialog(null, "Thêm thành công ");
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int position = tableMain.getSelectedRow();// lay vi tri row minh chon de xoa
        String data  = tableMain.getModel().getValueAt(position, 0).toString();
                              // getModel nội dung // ValueAt: bạn muốn lấy dòng nào, cột nào
                                                                            // cột "0" hiện là maNV
           int result = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắc muốn xóa không ?");
           if(result == 0){
            try {
                Connection connection = DBConnection.getConnection();
                String sql = "DELETE FROM dbo.[sanpham] WHERE IDSP = ?";

                PreparedStatement ps = connection.prepareStatement(sql);
                ps.setString(1,data);
         
            
                ps.executeUpdate();
                showDuLieu();
                DBConnection.closeConnection(connection);
                JOptionPane.showMessageDialog(null, "Xóa thành công ");
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
           }
    }//GEN-LAST:event_btnDeleteActionPerformed

