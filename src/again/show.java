    private void showDuLieu() {
        
        try {
        tableMain.removeAll();
        String[] arr = {"IDSP", "Tên sản phẩm", "Giá sản phẩm", "Số lượng"};
        DefaultTableModel model = new DefaultTableModel(arr, 0);
        
        Connection connection = DBConnection.getConnection();
        
        String sql = "SELECT * FROM sanpham";
        
        PreparedStatement ps = connection.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();
        
            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString("IDSP"));
                vector.add(rs.getString("tensp"));
                vector.add(rs.getString("gia"));
                vector.add(rs.getString("soLuong"));
                model.addRow(vector);
            }
            tableMain.setModel(model);

        } catch (SQLException ex) {
            Logger.getLogger(show.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
   
